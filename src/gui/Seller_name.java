    public void loadData() {
        try {
            ResultSet rs = MySQL.search("SELECT * FROM `gb_seller`;");

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {

                Vector v = new Vector();
                v.add(rs.getString("id"));
                v.add(rs.getString("name"));
                v.add(rs.getString("mobile"));
                dtm.addRow(v);
            }

        } catch (Exception e) {
            e.printStackTrace();

        }

    }

