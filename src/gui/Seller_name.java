    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) {
            int r = jTable1.getSelectedRow();

            if (r == -1) {
                JOptionPane.showMessageDialog(this, "Please select a Seller", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {

                String gbid = jTable1.getValueAt(r, 0).toString();
                String sname = jTable1.getValueAt(r, 1).toString();

                //pg.openproductarea();
                pg.balanceCheck(gbid);
                pg.jLabel58.setText(gbid);
                pg.jButton2.setText(sname);
                pg.jButton3.setEnabled(true);

            }
            this.dispose();
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String Newname = jTextField1.getText();
        String Newmobile = jTextField2.getText();
        if (Newname == null | Newname.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter seller name !", "Warning", JOptionPane.WARNING_MESSAGE);
        }else if (Newmobile == null | Newmobile.isEmpty()){
            JOptionPane.showMessageDialog(this, "Please enter seller mobile number !", "Warning", JOptionPane.WARNING_MESSAGE);
        }else if (!Pattern.compile("0[0-9]{9}").matcher(Newmobile).matches()){
            JOptionPane.showMessageDialog(this, "Please enter seller mobile number !", "Warning", JOptionPane.WARNING_MESSAGE);
        }else{
            MySQL.iud("INSERT INTO `gb_seller`(`name`,`mobile`) VALUES ('" + Newname + "','" + Newmobile + "');");
            loadData();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

