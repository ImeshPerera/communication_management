    public void loadProduct() {
        try {
            ResultSet rs = MySQL.search("SELECT `product`.`id`,`product`.`name`,`brand`.`name` AS `brand_name` From `product` LEFT JOIN `brand` ON `product`.`brand_id` = `brand`.`id`;");

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("id"));
                v.add(rs.getString("name"));
                v.add(rs.getString("brand_name"));
                dtm.addRow(v);
            }

        } catch (Exception e) {
            e.printStackTrace();

        }

    }

    public void loadProduct(String my_text) {
        text = my_text;
        try {
            ResultSet rs = MySQL.search("SELECT `product`.`id`,`product`.`name`,`brand`.`name` AS `brand_name` From `product` LEFT JOIN `brand` ON `product`.`brand_id` = `brand`.`id` WHERE `name` LIKE '%" + text + "%';");

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("id"));
                v.add(rs.getString("name"));
                v.add(rs.getString("brand_name"));
                dtm.addRow(v);
            }

        } catch (Exception e) {
            e.printStackTrace();

        }

    }
    

