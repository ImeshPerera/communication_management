    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String p_name = jTextField1.getText();
        String b_id = jLabel2.getText();
        String c_id = jLabel3.getText();

        if (p_name == null | p_name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Product name is empty !", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (Pattern.compile("[1-9]{1}[0-9]*").matcher(b_id).matches()) {
            MySQL.iud("INSERT INTO `product`(`name`,`brand_id`,`category_id`) VALUES ('" + p_name + "','" + b_id + "','" + c_id + "');");
            loadProduct();
        } else {
            JOptionPane.showMessageDialog(this, "Please select a Brand !", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

